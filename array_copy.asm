; Программа для копирования массива из 5 элементов
; Исходный массив: адреса 100-104
; Целевой массив: адреса 200-204

; Загружаем начальные адреса
LOAD_CONST 100    ; Адрес источника
LOAD_CONST 200    ; Адрес назначения
LOAD_CONST 5      ; Количество элементов

; Основной цикл копирования
; (здесь будет реализация цикла на ассемблере УВМ)
; Пока просто демонстрация работы с памятью

; Читаем значение из источника
LOAD_MEM 100      ; Читаем MEM[100]
STORE_MEM 100     ; Записываем по тому же адресу (тест)

; Еще тестовые операции
LOAD_CONST 50     ; Загружаем константу
STORE_MEM 0       ; Записываем по адресу 0

; Чтение нескольких значений
LOAD_MEM 133      ; MEM[133] = 42 (из тестовых данных)
LOAD_MEM 500      ; MEM[500] = 25
LOAD_MEM 501      ; MEM[501] = 100
