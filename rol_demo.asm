; Тестовая программа для демонстрации работы команды ROL (циклический сдвиг влево)
; Этап 4: Реализация АЛУ

; 1. Подготовка тестовых данных в памяти
; В адреса 1000-1005 записываем количество сдвигов для каждого теста
LOAD_CONST 1      ; 1 сдвиг
STORE_MEM 1000    ; MEM[1000] = 1

LOAD_CONST 2      ; 2 сдвига
STORE_MEM 1001    ; MEM[1001] = 2

LOAD_CONST 3      ; 3 сдвига
STORE_MEM 1002    ; MEM[1002] = 3

LOAD_CONST 8      ; 8 сдвигов (полный оборот)
STORE_MEM 1003    ; MEM[1003] = 8

LOAD_CONST 0      ; 0 сдвигов
STORE_MEM 1004    ; MEM[1004] = 0

LOAD_CONST 4      ; 4 сдвига
STORE_MEM 1005    ; MEM[1005] = 4

; 2. Тест 1: ROL с 1 сдвигом
; Значение: 0b10000001 (129) → после ROL(1): 0b00000011 (3)
LOAD_CONST 0b10000001  ; Значение для сдвига
LOAD_CONST 1000        ; Адрес, где лежит количество сдвигов (1)
ROL                    ; Выполняем сдвиг
STORE_MEM 2000         ; Сохраняем результат в MEM[2000]

; 3. Тест 2: ROL с 2 сдвигами
; Значение: 0b11001100 (204) → после ROL(2): 0b00110011 (51)
LOAD_CONST 0b11001100
LOAD_CONST 1001        ; Адрес с количеством сдвигов = 2
ROL
STORE_MEM 2001         ; MEM[2001] = результат

; 4. Тест 3: ROL с 3 сдвигами
; Значение: 0b10101010 (170) → после ROL(3): 0b01010101 (85)
LOAD_CONST 0b10101010
LOAD_CONST 1002        ; 3 сдвига
ROL
STORE_MEM 2002         ; MEM[2002] = результат

; 5. Тест 4: ROL с 8 сдвигами (полный оборот)
; Значение: 0b11110000 (240) → после ROL(8): 0b11110000 (240)
LOAD_CONST 0b11110000
LOAD_CONST 1003        ; 8 сдвигов
ROL
STORE_MEM 2003         ; MEM[2003] = результат (должен быть таким же)

; 6. Тест 5: ROL с 0 сдвигами
; Значение: 0b01010101 (85) → после ROL(0): 0b01010101 (85)
LOAD_CONST 0b01010101
LOAD_CONST 1004        ; 0 сдвигов
ROL
STORE_MEM 2004         ; MEM[2004] = результат

; 7. Тест 6: ROL с 4 сдвигами
; Значение: 0b00001111 (15) → после ROL(4): 0b11110000 (240)
LOAD_CONST 0b00001111
LOAD_CONST 1005        ; 4 сдвига
ROL
STORE_MEM 2005         ; MEM[2005] = результат

; 8. Чтение результатов для проверки
LOAD_MEM 2000    ; Результат теста 1 (должен быть 3)
LOAD_MEM 2001    ; Результат теста 2 (должен быть 51)
LOAD_MEM 2002    ; Результат теста 3 (должен быть 85)
LOAD_MEM 2003    ; Результат теста 4 (должен быть 240)
LOAD_MEM 2004    ; Результат теста 5 (должен быть 85)
LOAD_MEM 2005    ; Результат теста 6 (должен быть 240)
